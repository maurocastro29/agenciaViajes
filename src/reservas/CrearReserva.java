    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(tableCliente.getSelectedRow()>=0){
            if(!validarVacios()){
                String cedula = (String)tableCliente.getValueAt(tableCliente.getSelectedRow(), 0);
                String nombre = (String)tableCliente.getValueAt(tableCliente.getSelectedRow(), 1);
                String idReserva = jtID.getText();
                String tipo = (String)jcTipo.getSelectedItem();
                String valor = (String)jtValor.getText();
                Reserva reserva = new Reserva(idReserva, cedula, nombre, tipo, valor);
                fr.guardarDatosArchivo(reserva);
                JOptionPane.showMessageDialog(null, "Reserva creada con exito");
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Verifique que no queden campos vacios");
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "No ha elegido ningun usuario.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

